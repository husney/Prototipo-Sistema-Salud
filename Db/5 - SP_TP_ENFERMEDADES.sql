DROP PROCEDURE IF EXISTS STP_TP_ENFERMEDADES
GO

CREATE PROCEDURE STP_TP_ENFERMEDADES(
	@ID INT = NULL,	
	@DESCRIPCION VARCHAR(100) = NULL,
	@OPERACION TINYINT = NULL
)
AS
BEGIN

	SET NOCOUNT ON;
---------------------
-- INSERTAR
---------------------
	IF @OPERACION = 0
	BEGIN
		
		INSERT INTO
			TP_ENFERMEDADES
		(
			DESCRIPCION
		)
		VALUES
		(
			@DESCRIPCION
		)

	END

--------------------
-- ACTUALIZAR
--------------------
	IF @OPERACION = 1
	BEGIN
		
		UPDATE
			TP_ENFERMEDADES
		SET 
			DESCRIPCION = @DESCRIPCION
		WHERE
			ID = @ID
	END


--------------------
-- ELIMINAR
--------------------
	IF @OPERACION = 2
	BEGIN
		
		DELETE FROM
			TP_ENFERMEDADES
		WHERE 
			ID = @ID

	END

--------------------
-- SELECCIONAR UNO
--------------------
	
	IF @OPERACION = 3
	BEGIN
		
		SELECT
			ID,			
			DESCRIPCION
		FROM 
			TP_ENFERMEDADES
		WHERE 
			ID = @ID

	END

---------------------
-- SELECCIONAR TODOS
---------------------

	IF @OPERACION = 4
	BEGIN
		
		SELECT
			ID,			
			DESCRIPCION
		FROM 
			TP_ENFERMEDADES

	END

END